				<h1>Market Segments</h1>
				

				<p>The project aims to curve the space between the producers and consumers in the market, throughout technology and good work.</p>

				<p>Trying to shorten the distance between producer and consumer might seem counterproductive for a project based on producer and consumer. However, it allows us to obtain many benefits that can be interpreted as a great competitive strength.</p>    
				<p>We will assure the loyalty of producer and of consumer. The producer will obtain a bigger amount of benefits from his or her job and the quality and originality of the product will be guaranteed to the consumer.</p>

				<p>From this perspective, producer and consumer are clients, because this is how we work: throughout the payment of commissions, obtained after each transaction is finished.</p>

				<p>Therefore, our main goal is to focus the effort in generating a higher volume of transactions of lower commission.</p>

				<p>In order to assure the desired sales volume, we will diversify, as much as possible, the market segments with which we are going to work.</p>

