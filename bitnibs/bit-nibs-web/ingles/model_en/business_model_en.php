				<h1>Introduction to the Business Model of the Bit&amp;Nibs Project</h1>

				<h2>Main Goal:</h2>

				<p>The Bit&amp;Nibs Project originated thanks to the change of conscience that occurred when disruptive technologies were created and shared around the world. Some of the most important examples of these technologies are: the block chain, the bitcoin, the 3D printers and the smart contracts.</p>

				<p>But the Bit&amp;Nibs Project not only has grown thanks to these technologies, but it has also grown thanks to the movements originated by the Internet’s users’ new forms of interaction with these technologies. Concepts such as crowdsourcing, prosumers, bootstrapping, and crowdfunding have inspired us and they have also convinced us of the effectiveness of the work done from different perspectives. </p>

				<p>This important change of conscience in the society has motivated us to create a project that includes not only the desirable characteristics of a company built during the third industrial revolution, but also a company that could include the biggest amount of people. These people will gain courage and will be able to show this courage inside a company where everyone has the opportunity to feel like the owner. </p>
			 
				<p>Today the world is passing through one of its most confusing times. We found ourselves caught between the fear brought by the economic crisis and the hope we have in the many opportunities the new technologies can bring us.</p>

				<p>In this context, the most reasonable answer is not to panic and not to be paralyzed by fear. In this way, the solution to our problems begins with a proposal to change, to improve our lives and the lives of our clients.</p>

				<p>The main strategy of Bit&amp;Nibs is the distribution and selling of Latin American cocoa in Europe. However, we believe that we can create a new paradigm in the way of doing business, and we believe that this new paradigm can work efficiently in other contexts.</p>

				<p> The Bit&amp;Nibs project will open the opportunity for people to obtain money. But more than that, this project represents the search of a more transparent and fair business model: a business model that can influence other spheres of society, and, therefore, we will adopt business models that will benefit everyone.</p>

