			<div id="canales">
					<div class="info" title="This part analyzes who does a company reaches the chosen client market (#1) to bring them the value proposition (#2)"></div>
					<h1>Channels</h1>
					
					<p>Logistics is one of the many important aspects to consider when taking part in any successful project, particularly regarding the way in which the clients are going to obtain the products without regards to the location of the client (delivery). </p>
					<p>This aspect gains more importance when considering a company that focuses on the distribution and selling of products created by a third party.</p>
					<p>That is why throughout our business model we will make every effort to explore different types of channels, in order to develop our own efficient delivery channel.</p> 

					<p><strong>Direct Channel:</strong></p>
						<p>Using this channel we will try to reach our clients by means of a direct interaction. This channel is more expensive for the client, but it will give us the best opportunity to obtain the client’s feedback. It will be a service offered in our webpage that will include the mail service to deliver the products the client acquired.</p>

					<p><strong>Indirect Channel:</strong></p>
						<p>This is the main channel we will use in the case of handmade chocolate, because it will be better to sell them in our specialized stores than from our webpage. In this type of channel we hope that the business relationship with the prosumers will be strengthened. The prosumers could easily become our local allies for the distribution of the product.</p>

					<p><strong>Partners Channels:</strong></p>
						<p> Since the company is “decentralized”, the owners—holders— will be located around the world. Each holder will have the possibility (with privileges) to distribute regionally all the products available in the company according to the convenience and compliance of the local laws.</p> 

					<p>All the channels will have a single communicative strategy. Our advertisements will be done mainly throughout internet. Once the client interacts with our web page, the client will be able to deeply evaluate our value proposal (and in an entertaining way).</p>

					<p>Regarding the after-sales, we want to keep in touch with our clients in order to know the client’s opinion related to our products, new business proposals and improvements.</p>

