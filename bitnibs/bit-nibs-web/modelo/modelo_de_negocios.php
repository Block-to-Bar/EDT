				<h1>Introducción al Modelo de Negocio del Proyecto Bit&amp;Nibs</h1>

				<h2>Objetivo:</h2>

				<p>El proyecto Bit&amp;Nibs nació bajo el contexto de un cambio de conciencia de época motivado por el nacimiento y propagación de las llamadas tecnologías disruptivas, tales como: la cadena de bloques, el bitcoin, las impresoras 3D y los contratos inteligentes, sólo por mencionar algunas.</p>

				<p>El proyecto Bit&amp;Nibs no se ha nutrido sólo de estas tecnologías, sino también de conceptos y movimientos originados por las nuevas formas de interacción de los usuarios con las tecnologías en el ámbito del internet. Conceptos como el <em>crowdsourcing</em>, prosumidores, <em>bootstraping</em> y <em>crowdfunding</em> nos han inspirado, y a la vez convencido de que el trabajo enfocado desde distintas perspectivas nos puede llevar a grandes logros.</p>

				<p>Este notorio cambio de conciencia en la sociedad nos motivó a crear un proyecto que no sólo incluyera todas las características deseables de una compañía de la era de la tercera revolución industrial, sino que además pudiera incluir la mayor cantidad de personas posibles, con el fin de permitirles generar valor y  poderlo proyectar dentro de una empresa en la que todo interesado se pueda sentir dueño.</p>
			 
				<p>Hoy día el mundo está viviendo uno de sus momentos más convulsos. Nos encontramos atrapados entre el miedo por la crisis económica y la esperanza depositada en las posibilidades que nos brindan las nuevas tecnologías.</p>

				<p>En este contexto, la solución más razonable es no dejarse llevar por el pánico, ni dejar que la incertidumbre nos paralice. De este modo, la solución comienza con la propuesta de un cambio para mejorar nuestras vidas y la de nuestros clientes.</p>

				<p>La estrategia principal del proyecto Bit&amp;Nibs es la distribución y venta de cacao latinoamericano en Europa. No obstante, tenemos plena confianza en poder crear un nuevo paradigma en la forma de hacer negocios, la cual pudiera ser aplicado a diversos sectores.</p>

				<p>El proyecto Bit&amp;Nibs no sólo será una plataforma para que personas tengan la posibilidad de generar ingresos extra, sino que el proyecto también, representa una búsqueda de modelo de negocio más transparente y justo, que pueda influenciar otras esferas de la sociedad, con el fin de adoptar modelos de negocios que beneficien a todos sin excepción.</p>

				<br>

