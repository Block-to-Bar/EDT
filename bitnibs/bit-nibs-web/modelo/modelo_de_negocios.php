					<h1>Canales</h1>
					
					<p>El aspecto clave de todo proyecto es la logística; cómo hacer llegar los productos a todos los clientes sea donde sea que se encuentren.</p>
					<p>Este aspecto es aún más importante cuando se trata de una empresa que se basa en la distribución y venta de productos creados por terceros.</p>
					<p>Es por eso que en nuestro modelo de negocios no escatimaremos en la exploración de diversas tipologías de canales, con el fin de llegar a desarrollar un canal propio y eficiente en las entregas.</p> 

					<p><strong>Canal directo:</strong></p>
						<p>Por medio de este tipo de canal buscaremos llegar al cliente mediante una interacción directa. Éste es más costoso para el cliente, pero también es el medio que asegura la obtención del feeedback del cliente de la mejor manera. Se puede entender como en el caso de un servicio que se genera desde nuestra página web, a partir del cual se utiliza el servicio de correo para entregar lo que el cliente adquirió.</p>

					<p><strong>Canal indirecto:</strong></p>
						<p>Este es el principal canal a desarrollar en el caso de chocolates artesanales, los cuales se venderían mejor en tiendas especializadas que desde nuestra página web. En este tipo de canal se contempla el fortalecimiento de la relación comercial con los prosumidores, los cuales podrían fácilmente convertirse en aliados locales para la distribución del producto.</p>

					<p><strong>Canales de Socios:</strong></p>
						<p>Dado el carácter "descentralizado" de la compañía, los dueños –accionistas- de esta se hallaran distribuidos en diferentes partes del mundo. Cada accionista tendrá la posibilidad (con privilegios) de distribuir regionalmente todos los productos con los que trabaja el proyecto en base a la conveniencia y cumplimiento de las normativas locales.</p> 

					<p>Todos los canales compartirán una estrategia de comunicación  en común, ya que  nuestra vía de promoción se dará principalmente a través de internet. Una vez el cliente interactúe con nuestra página web podrá evaluar con profundidad (y de manera entretenida) nuestra propuesta de valor.</p>

					<p>En relacion a la post-venta buscamos mantener un contacto directo con nuestro cliente con el fin de conocer su opinión acerca nuestros productos, propuestas que querrían implementar o mejoras sugeridas.</p>

